
public class Exercicio_2_segundoPrograma {
    public static void main(String[] args) {
        int x = 10;
        int contador = 0;

        while (contador < 20) {
            System.out.println("Valor de x: " + x);
            x++; // Incrementa o valor de x em 1 a cada iteração
            contador++; // Incrementa o contador para controlar o número de iterações
        }
    }
}
